package com.tradingplatform.listing.enums;

/**
 * Enumeration for item condition in marketplace listings.
 */
public enum Condition {
    /**
     * Brand new, never used, original packaging intact.
     */
    NEW,

    /**
     * Like new, barely used, no visible wear.
     */
    LIKE_NEW,

    /**
     * Good condition, normal wear from regular use.
     */
    GOOD,

    /**
     * Fair condition, noticeable wear but fully functional.
     */
    FAIR,

    /**
     * Poor condition, significant wear, may need repairs.
     */
    POOR
}
